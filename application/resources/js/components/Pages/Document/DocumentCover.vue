      <h2>紐付けの意味</h2>
      <p>ニーズとその上位に位置するアイデアの紐付けは提案のようなものです。</p>
      <p>「街を綺麗にしたい」というニーズに対して、アイデアである「ゴミ拾いをする」が提案されます。</p>
