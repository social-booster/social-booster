      <p>コミュニティが「ゴミ拾いをする」アイデアを具体化したプロジェクトと紐付けられると、紐付けられたコミュニティがそのプロジェクトの実行者となります。</p>
      <p>このように、上位に対する紐付けは問題を解決するための流れだと言えます。</p>
      <p>逆に下位への紐付けやその確認は、上位のコンセプトに本当に意義があるのかを調べる流れです。</p>
      <p>ストレスやニーズと紐付きの少ないアイデア、プロジェクトには社会的な意義は無く、そこに重きを置くコミュニティがあればそのコミュニティは社会に貢献していないことを表します。</p>
