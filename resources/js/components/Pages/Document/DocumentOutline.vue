    <p>有意義なコンセプトとの紐付けが集まれば、それに投票するユーザーが現れます。SocialBoosterはそれらを集計して対応の度合いを算出します。</p>
    <p>多くのユーザーに投票されるほど有意義な紐付けがされるようになればそのコンセプトの表示順位・優先順位は下がります。</p>
