    <h2>優先順位</h2>
    <p>SocialBoosterは投票を元にそのコンセプトが社会にどれだけ期待されているかを算出します。同時に、そのコンセプトがどれだけ対応されているかを算出し、されていなければ優先順位を高くします。</p>
    <p>優先順位と表示順位は同じものです。優先順位が高い程ホーム画面で上位に表示されます。</p>
    <p>ユーザー、そして社会はホーム画面の上位に表示されている優先順位の高いコンセプトに対応しなくてはなりません。</p>
    <p>何をもって対応とするかはコミュニティとストレス～プロジェクトで異なります。</p>
    <h3>コミュニティの場合</h3>
    <p>コミュニティが上位に表示されている場合、そのコミュニティに向けられている期待と活動の規模が相当していない事を表します。</p>
    <p>活動の規模はそのコミュニティに参加しているユーザーの数から算出します。期待とコミュニティの参加者数が相当していないとき、コミュニティの優先順位は高くなります。</p>
    <p>もしあなたがこのコミュニティに貢献できるのであれば是非参加してください。参加者数が期待に相当する程になるとコミュニティの表示順位・優先順位は自ずと下がっていきます。</p>
    <h3>ストレス～プロジェクトの場合</h3>
